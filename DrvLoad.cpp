BOOL LoadNTDriver(WCHAR* lpszDriverName, WCHAR* lpszDriverPath, int servicetype, int boottype, WCHAR* groupname)
{
	WCHAR szshow[256];
	WCHAR szDriverImagePath[256];
	//?????????
	GetFullPathNameW(lpszDriverPath, 256, szDriverImagePath, NULL);

	char szfullpath[1024] = { 0 };
	int len = WideCharToMultiByte(CP_ACP, 0, szDriverImagePath, -1, szfullpath, sizeof(szfullpath), 0, 0);
	szfullpath[len] = 0;

	BOOL bRet = FALSE;

	SC_HANDLE hServiceMgr = NULL;
	SC_HANDLE hServiceDDK = NULL;


	hServiceMgr = OpenSCManagerW(NULL, NULL, SC_MANAGER_ALL_ACCESS);

	if (hServiceMgr == NULL)
	{
		bRet = FALSE;
		goto BeforeLeave;
	}

	//??????????
	hServiceDDK = CreateServiceW(hServiceMgr,
		lpszDriverName, //?????????????
		lpszDriverName, // ???????? DisplayName ?
		SERVICE_ALL_ACCESS, // ???????????
		servicetype,
		//SERVICE_KERNEL_DRIVER,// ????????????
		boottype,
		//SERVICE_AUTO_START, // ???????? Start ?
		SERVICE_ERROR_NORMAL, // ???????? ErrorControl ?
		szDriverImagePath, // ???????? ImagePath ?
		groupname,
		NULL,
		NULL,
		NULL,
		NULL);

	DWORD dwRtn;
	//????????
	if (hServiceDDK == NULL)
	{
		dwRtn = GetLastError();
		if (dwRtn != ERROR_IO_PENDING && dwRtn != ERROR_SERVICE_EXISTS)
		{
			bRet = FALSE;
			goto BeforeLeave;
		}

		// ????????,?????
		hServiceDDK = OpenServiceW(hServiceMgr, lpszDriverName, SERVICE_ALL_ACCESS);
		if (hServiceDDK == NULL)
		{
			//?????????,?????
			dwRtn = GetLastError();

			bRet = FALSE;
			goto BeforeLeave;
		}
	}

	//??????
	bRet = StartServiceW(hServiceDDK, 0, 0);
	if (!bRet)
	{
		DWORD dwRtn = GetLastError();
		if (dwRtn != ERROR_IO_PENDING && dwRtn != ERROR_SERVICE_ALREADY_RUNNING)
		{
			MessageBoxW(0, szshow, szshow, MB_OK);

			bRet = FALSE;
			goto BeforeLeave;
		}
		else
		{
			if (dwRtn == ERROR_IO_PENDING)
			{
				//?????
				bRet = FALSE;
				goto BeforeLeave;
			}
			else
			{
				//??????
				bRet = TRUE;
				goto BeforeLeave;
			}
		}
	}
	bRet = TRUE;
	//???????
BeforeLeave:
	if (hServiceDDK)
	{
		CloseServiceHandle(hServiceDDK);
	}
	if (hServiceMgr)
	{
		CloseServiceHandle(hServiceMgr);
	}
	return bRet;
}
bool AdminOrUser()
{
	bool isAdmin = false;
	HANDLE hToken = NULL;
	if (OpenProcessToken(GetCurrentProcess(), TOKEN_QUERY, &hToken))
	{
		TOKEN_ELEVATION elevation;
		DWORD dwSize;
		if (GetTokenInformation(hToken, TokenElevation, &elevation, sizeof(elevation), &dwSize))
		{
			isAdmin = (elevation.TokenIsElevated != 0);
		}
		CloseHandle(hToken);
	}
	return isAdmin;
}
